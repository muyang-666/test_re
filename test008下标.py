str1 = 'abcdefg'

# 打印字符串中最开始的字符
print(str1[0]) #a

# 打印最后一个位置的数据
print(str1[-1]) #g

# 打印倒数第二个位置的字符
print(str1[-2]) #f

print(str1[0:3])#abc
print(str1[:3])#abc

# efg
print(str1[4:7])
print(str1[-3:7])
print(str1[4:])

print(str1[:])# abcdefg

# aceg
print(str1[::2])

# 逆置 字符串 gfedcba
print(str1[::-1])
